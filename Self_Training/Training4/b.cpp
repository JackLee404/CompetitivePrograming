#include <bits/stdc++.h>

using i64 = long long;

// n! is the multiple of number from (1, n) 
// x! + (x - 1)! is the multiple of number from (1, n - 1) and n + 1

void solve() {
	i64 k;
	std::cin >> k;

	std::cout << k - 1 << std::endl;
}

int main() {
	int _;
	std::cin >> _;
	while(_ --) {
		solve();
	}
}